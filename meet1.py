# Membandingkan dua nilai: x dan y

# Inisialisasi variabel
x = 24
y = 56

# Menampilkan nilai awal
print(f"Nilai x = {x}")
print(f"Nilai y = {y}\n")

# Operasi perbandingan dan hasilnya
hasil = x < y
print(f"Apakah x < y? {hasil}")

hasil = x > y
print(f"Apakah x > y? {hasil}")

hasil = x <= y
print(f"Apakah x <= y? {hasil}")

hasil = x >= y
print(f"Apakah x >= y? {hasil}")

hasil = x == y
print(f"Apakah x == y? {hasil}")

hasil = x != y
print(f"Apakah x != y? {hasil}")
